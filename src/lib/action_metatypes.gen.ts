import type {Gen} from '@ryanatkn/gro/gen.js';
import {z} from 'zod';

import {get_innermost_type} from '$lib/zod_helpers.js';
import {action_specs} from '$lib/action_collections.js';
import {Action_Registry} from '$lib/action_registry.js';
import {
	to_action_spec_params_identifier,
	to_action_spec_result_identifier,
	to_action_message_type,
	to_action_request_message_type,
	to_action_response_message_type,
} from '$lib/action_helpers.js';
import type {Action_Method} from '$lib/action_metatypes.js';
import type {Action_Request_Response_Flag} from '$lib/action_types.js';

/**
 * Outputs a file with generated types and schemas using the action specs as the source of truth.
 */
export const gen: Gen = ({origin_path}) => {
	const registry = new Action_Registry(action_specs);

	// Get all schema names that need to be imported
	const schema_imports = registry.get_schema_imports();

	const banner = `generated by ${origin_path} - DO NOT EDIT OR RISK LOST DATA`;

	return `
		// ${banner}

		import {z} from 'zod';

		import type {Client_Action_Handler} from '$lib/client_action_handler.js';
		import type {${schema_imports.join(', ')}} from '$lib/action_specs.js';
		import type { Zzz } from '$lib/zzz.svelte.js';

		/**
		 * All action method names. Request/response actions have two types per method.
		 */
		export const Action_Method = z.enum([
			${registry.specs.map(({method}) => `'${method}'`).join(',\n\t')}
		]);
		export type Action_Method = z.infer<typeof Action_Method>;

		/**
		 * Names of all request_response actions.
		 */
		export type Request_Response_Action_Method = ${
			registry.request_response_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * Names of all server_notification actions.
		 */
		export type Server_Notification_Action_Method = ${
			registry.server_notification_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * Names of all client_local actions.
		 */
		export type Client_Local_Action_Method = ${
			registry.client_local_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * All action types. May have '/' separators for namespacing.
		 * TODO maybe any relative url-encoded path is valid? add to schema defs
		 */
		export const Action_Message_Type = z.enum([
			${registry.specs
				.map((spec) =>
					spec.kind === 'request_response'
						? [
								`'${to_action_message_type(spec.method, 'request')}'`,
								`'${to_action_message_type(spec.method, 'response')}'`,
							]
						: `'${spec.method}'`,
				)
				.flat()
				.join(',\n\t')}
		]);
		export type Action_Message_Type = z.infer<typeof Action_Message_Type>;

		export interface Action_Message_Params {
			${registry.specs
				.map((spec) => {
					const {method} = spec;
					if (spec.kind === 'request_response') {
						return `${to_action_request_message_type(method)}: z.infer<typeof ${to_action_spec_params_identifier(method)}>,
						${to_action_response_message_type(method)}: z.infer<typeof ${to_action_spec_result_identifier(method)}>`;
					} else {
						return `${method}: z.infer<typeof ${to_action_spec_params_identifier(method)}>`;
					}
				})
				.join(',\n\t\t\t')}
		}

		/**
		 * Interface for action dispatch functions.
		 */
		export interface Actions_Api {
			${registry.specs
				.map((spec) => {
					const innermost_type_name = get_innermost_type(spec.params)._def.typeName;
					const has_params =
						innermost_type_name !== z.ZodFirstPartyTypeKind.ZodNull &&
						innermost_type_name !== z.ZodFirstPartyTypeKind.ZodVoid;
					return `${spec.method}: (${
						has_params
							? `params${spec.params.isOptional() ? '?' : ''}: Action_Message_Params['${to_action_message_type(
									spec.method,
									spec.kind === 'request_response' ? 'request' : null,
								)}']`
							: 'params?: void'
					}) => ${
						spec.kind === 'request_response'
							? `Promise<Action_Message_Params['${to_action_response_message_type(spec.method)}']>`
							: spec.kind === 'client_local'
								? spec.returns
								: 'void'
					};`;
				})
				.join('\n\t')}
		}

		// TODO maybe extract to $lib/mutation_types.ts or similar, decoupled
		/**
		 * Interface for client-side mutation handlers.
		 */
		export interface Client_Action_Handlers<T_App extends Zzz = Zzz> extends Partial<Record<Action_Message_Type, Client_Action_Handler<T_App>>> {
			${registry.specs
				.map((spec) => {
					const v = (m: Action_Method, request_response_flag: Action_Request_Response_Flag) =>
						`${to_action_message_type(
							m,
							request_response_flag,
						)}?: Client_Action_Handler<T_App, Action_Message_Params['${to_action_message_type(
							spec.method,
							spec.kind === 'request_response' ? 'request' : null,
						)}'], ${
							request_response_flag === 'response'
								? `Action_Message_Params['${to_action_response_message_type(spec.method)}']`
								: 'void'
						}>;`;
					return spec.kind === 'request_response'
						? [v(spec.method, 'request'), v(spec.method, 'response')]
						: v(spec.method, null);
				})
				.flat()
				.join('\n\t')}
		}

		// ${banner}
	`;
};
