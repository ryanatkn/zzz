import type {Gen} from '@ryanatkn/gro/gen.js';
import {z} from 'zod';

import {get_innermost_type_name} from '$lib/zod_helpers.js';
import {action_specs} from '$lib/action_collections.js';
import {Action_Registry} from '$lib/action_registry.js';
import {
	to_action_request_message_type,
	to_action_response_message_type,
	to_action_message_type,
} from '$lib/action_helpers.js';
import {ACTION_KIND_PHASES, type Action_Phase} from '$lib/action_types.js';

/**
 * Outputs a file with generated types and schemas using the action specs as the source of truth.
 */
export const gen: Gen = ({origin_path}) => {
	const registry = new Action_Registry(action_specs);

	const banner = `generated by ${origin_path} - DO NOT EDIT OR RISK LOST DATA`;

	// TODO BLOCK @api probably extract a helper to avoid repetition
	// Helper to generate phase handlers based on ACTION_KIND_PHASES
	const generate_phase_handlers = (spec: (typeof registry.specs)[0]): string => {
		const {method, kind} = spec;
		const phases = ACTION_KIND_PHASES[kind];

		const phase_handlers = phases
			.map((phase: Action_Phase) => {
				// Determine input and output types based on phase
				let input_type: string;
				let output_type: string;

				switch (phase) {
					case 'send_request':
					case 'receive_request':
						input_type = `Action_Inputs['${to_action_request_message_type(method)}']`;
						output_type =
							phase === 'receive_request' && kind === 'request_response'
								? `Action_Inputs['${to_action_response_message_type(method)}']`
								: 'void';
						break;
					case 'send_response':
					case 'receive_response':
						input_type =
							kind === 'request_response'
								? phase === 'send_response'
									? `Action_Inputs['${to_action_response_message_type(method)}']`
									: `Action_Inputs['${to_action_request_message_type(method)}']`
								: 'never';
						output_type =
							kind === 'request_response'
								? phase === 'receive_response'
									? `Action_Inputs['${to_action_response_message_type(method)}']`
									: 'void'
								: 'never';
						break;
					case 'send':
					case 'receive':
						input_type = `Action_Inputs['${method}']`;
						output_type = 'void';
						break;
					case 'execute':
						input_type = `Action_Inputs['${method}']`;
						output_type = spec.returns || 'void';
						break;
					default:
						input_type = 'never';
						output_type = 'never';
				}

				return `${phase}?: Client_Action_Handler<
			T_App,
			${input_type},
			${output_type}
		>`;
			})
			.join(';\n\t\t');

		return `${method}?: {\n\t\t${phase_handlers};\n\t}`;
	};

	return `
		// ${banner}

		import {z} from 'zod';

		import type {Client_Action_Handler} from '$lib/client_action_handler.js';
		import type {Action_Inputs, Action_Outputs} from '$lib/action_collections.js';
		import type {Zzz_App} from '$lib/zzz_app.svelte.js';

		/**
		 * All action method names. Request/response actions have two types per method.
		 */
		export const Action_Method = z.enum([
			${registry.specs.map(({method}) => `'${method}'`).join(',\n\t')}
		]);
		export type Action_Method = z.infer<typeof Action_Method>;

		/**
		 * Names of all request_response actions.
		 */
		export type Request_Response_Action_Method = ${
			registry.request_response_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * Names of all remote_notification actions.
		 */
		export type Remote_Notification_Action_Method = ${
			registry.remote_notification_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * Names of all local_call actions.
		 */
		export type Local_Call_Action_Method = ${
			registry.local_call_specs.map((spec) => `'${spec.method}'`).join(' | ') || 'never'
		};

		/**
		 * All action types. May have '/' separators for namespacing.
		 * TODO maybe any relative url-encoded path is valid? add to schema defs
		 */
		export const Action_Message_Type = z.enum([
			${registry.specs
				.map((spec) =>
					spec.kind === 'request_response'
						? [
								`'${to_action_message_type(spec.method, 'request')}'`,
								`'${to_action_message_type(spec.method, 'response')}'`,
							]
						: `'${spec.method}'`,
				)
				.flat()
				.join(',\n\t')}
		]);
		export type Action_Message_Type = z.infer<typeof Action_Message_Type>;

		/**
		 * Interface for action dispatch functions.
		 */
		export interface Actions_Api {
			${registry.specs
				.map((spec) => {
					const innermost_type_name = get_innermost_type_name(spec.input);
					const has_input =
						innermost_type_name !== z.ZodFirstPartyTypeKind.ZodNull &&
						innermost_type_name !== z.ZodFirstPartyTypeKind.ZodVoid;
					return `${spec.method}: (${
						has_input
							? `input${spec.input.isOptional() ? '?' : ''}: Action_Inputs['${spec.method}']`
							: 'input?: void'
					}) => ${
						spec.kind === 'request_response'
							? `Promise<Action_Outputs['${spec.method}']>`
							: spec.kind === 'local_call'
								? spec.returns
								: 'void'
					};`;
				})
				.join('\n\t')}
		}

		/**
		 * Interface for client-side action handlers organized by method and phase.
		 */
		export interface Client_Action_Handlers<T_App extends Zzz_App = Zzz_App> {
			${registry.specs.map(generate_phase_handlers).join(';\n\t')}
		}

		// ${banner}
	`;
};
