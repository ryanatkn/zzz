import type {Gen} from '@ryanatkn/gro/gen.js';

import {action_specs} from '$lib/action_collections.js';
import {Action_Registry} from '$lib/action_registry.js';
import {
	to_action_spec_identifier,
	to_action_spec_input_identifier,
	to_action_spec_output_identifier,
	to_action_request_message_type,
	to_action_response_message_type,
} from '$lib/action_helpers.js';

/**
 * Outputs a file with action message schema definitions that can be imported by other modules.
 * This is separate from `action_metatypes.gen.ts` to avoid circular dependencies.
 */
export const gen: Gen = ({origin_path}) => {
	const registry = new Action_Registry(action_specs);

	const banner = `generated by ${origin_path} - DO NOT EDIT OR RISK LOST DATA`;

	return `
		// ${banner}

		import {z} from 'zod';

		import {Action_Message_Base} from '$lib/action_message.js';
		import {${registry.specs
			// TODO maybe change to `import * as specs`
			.map((spec) => to_action_spec_identifier(spec.method))
			.join(', ')}} from '$lib/action_specs.js';

		export const Action_Messages = {
			${registry.specs
				.map((spec) => {
					const {method} = spec;
					if (spec.kind === 'request_response') {
						return `${to_action_request_message_type(method)}: Action_Message_Base.extend({
						type: z.literal('${to_action_request_message_type(method)}').default('${to_action_request_message_type(method)}'),
						method: z.literal('${method}').default('${method}'),
							params: ${to_action_spec_input_identifier(method)},
						}),
						${to_action_response_message_type(method)}: Action_Message_Base.extend({
						type: z.literal('${to_action_response_message_type(method)}').default('${to_action_response_message_type(method)}'),
						method: z.literal('${method}').default('${method}'),
							params: ${to_action_spec_output_identifier(method)},
						})`;
					} else {
						return `${method}: Action_Message_Base.extend({
						type: z.literal('${method}').default('${method}'),
						method: z.literal('${method}').default('${method}'),
							params: ${to_action_spec_input_identifier(method)},
						})`;
					}
				})
				.join(',\n\t\t\t')}
		};
		export interface Action_Messages {
			${registry.specs
				.map((spec) => {
					const {method} = spec;
					if (spec.kind === 'request_response') {
						return `${to_action_request_message_type(method)}: z.infer<typeof Action_Messages.${to_action_request_message_type(method)}>,
						${to_action_response_message_type(method)}: z.infer<typeof Action_Messages.${to_action_response_message_type(method)}>`;
					} else {
						return `${method}: z.infer<typeof Action_Messages.${method}>`;
					}
				})
				.join(';\n\t\t\t')}
		}

		// TODO BLOCK @api use the following or a variant and delete the stuff above
		/**
		 * Action parameter schemas indexed by method name.
		 * These represent the input data for each action.
		 */
		export const Action_Inputs = {
			${registry.specs
				.map((spec) => `${spec.method}: ${to_action_spec_input_identifier(spec.method)}`)
				.join(',\n\t\t\t')}
		} as const;

		/**
		 * Action result schemas indexed by method name.
		 * These represent the output data for each action.
		 * Note: null outputs are represented as z.null() schemas.
		 */
		export const Action_Outputs = {
			${registry.specs
				.map((spec) => `${spec.method}: ${to_action_spec_output_identifier(spec.method)}`)
				.join(',\n\t\t\t')}
		} as const;

		/**
		 * Helper type to get params type for a method.
		 */
		export type Action_Input_For<T_Method extends keyof typeof Action_Inputs> = 
			z.infer<typeof Action_Inputs[T_Method]>;

		/**
		 * Helper type to get result type for a method.
		 */
		export type Action_Output_For<T_Method extends keyof typeof Action_Outputs> = 
			z.infer<typeof Action_Outputs[T_Method]>;

		/**
		 * Type guard to check if a method has params.
		 */
		export const has_action_params = (method: string): method is keyof typeof Action_Inputs => {
			return method in Action_Inputs;
		};

		/**
		 * Type guard to check if a method has results.
		 */
		export const has_action_result = (method: string): method is keyof typeof Action_Outputs => {
			return method in Action_Outputs;
		};

		/**
		 * Parse action params with validation.
		 */
		export const parse_action_input = <T_Method extends keyof typeof Action_Inputs>(
			method: T_Method,
			data: unknown
		): Action_Input_For<T_Method> => {
			return Action_Inputs[method].parse(data);
		};

		/**
		 * Parse action result with validation.
		 */
		export const parse_action_output = <T_Method extends keyof typeof Action_Outputs>(
			method: T_Method,
			data: unknown
		): Action_Output_For<T_Method> => {
			return Action_Outputs[method].parse(data);
		};

		/**
		 * Safe parse action params.
		 */
		export const safe_parse_action_input = <T_Method extends keyof typeof Action_Inputs>(
			method: T_Method,
			data: unknown
		): z.SafeParseReturnType<unknown, Action_Input_For<T_Method>> => {
			return Action_Inputs[method].safeParse(data);
		};

		/**
		 * Safe parse action result.
		 */
		export const safe_parse_action_output = <T_Method extends keyof typeof Action_Outputs>(
			method: T_Method,
			data: unknown
		): z.SafeParseReturnType<unknown, Action_Output_For<T_Method>> => {
			return Action_Outputs[method].safeParse(data);
		};

		// ${banner}
	`;
};
