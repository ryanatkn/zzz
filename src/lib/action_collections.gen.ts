import type {Gen} from '@ryanatkn/gro/gen.js';

import {action_specs} from '$lib/action_collections.js';
import {Action_Registry} from '$lib/action_registry.js';
import {
	to_action_spec_input_identifier,
	to_action_spec_output_identifier,
} from '$lib/action_helpers.js';

/**
 * Outputs a file with action collection types that can be imported by schemas.ts.
 * This is separate from `action_metatypes.gen.ts` to avoid circular dependencies.
 */
export const gen: Gen = ({origin_path}) => {
	const registry = new Action_Registry(action_specs);

	// TODO hacky
	const {methods, networked_methods, nonnetworked_methods} = registry;

	const banner = `generated by ${origin_path} - DO NOT EDIT OR RISK LOST DATA`;

	return `
		// ${banner}

		import {z} from 'zod';

		import {type Action_Spec, collect_action_specs} from '$lib/action_spec.js';
		import * as specs from '$lib/action_specs.js';
		import type {Action_Method} from '$lib/action_metatypes.js';
		import type {
			Request_Response_Action_Event_Data,
			Remote_Notification_Action_Event_Data,
			Local_Call_Action_Event_Data,
		} from '$lib/action_event_types.js';

		// TODO consistent naming
		/**
		 * All method types combined.
		 */
		export const Action_Method_Any = z.enum([
			${methods.map((method) => `'${method}'`).join(',\n\t\t\t')}
		]);
		export type Action_Method_Any = z.infer<typeof Action_Method_Any>;

		/**
		 * Method types with network endpoints.
		 */
		export const Action_Method_Networked = z.enum([
			${networked_methods.map((method) => `'${method}'`).join(',\n\t\t\t')}
		]);
		export type Action_Method_Networked = z.infer<typeof Action_Method_Networked>;

		/**
		 * Method types without network endpoints.
		 */
		export const Action_Method_Nonnetworked = z.enum([
			${nonnetworked_methods.map((method) => `'${method}'`).join(',\n\t\t\t')}
		]);
		export type Action_Method_Nonnetworked = z.infer<typeof Action_Method_Nonnetworked>;

		export const action_specs: Array<Action_Spec> = collect_action_specs(specs);
		
		export const action_spec_by_method: Map<Action_Method, Action_Spec> = new Map(action_specs.map((spec) => [spec.method, spec]));

		/**
		 * Action parameter schemas indexed by method name.
		 * These represent the input data for each action,
		 * e.g. JSON-RPC request/notification params and local call arguments.
		 */
		export const Action_Inputs = {
			${registry.specs
				.map((spec) => `${spec.method}: specs.${to_action_spec_input_identifier(spec.method)}`)
				.join(',\n\t\t\t')}
		} as const;
		export interface Action_Inputs {
			${registry.specs
				.map(
					(spec) =>
						`${spec.method}: z.infer<typeof specs.${to_action_spec_input_identifier(spec.method)}>`,
				)
				.join(';\n\t\t\t')}
		}

		/**
		 * Action result schemas indexed by method name.
		 * These represent the output data for each action,
		 * e.g. JSON-RPC response results and local call return values.
		 */
		export const Action_Outputs = {
			${registry.specs
				.map((spec) => `${spec.method}: specs.${to_action_spec_output_identifier(spec.method)}`)
				.join(',\n\t\t\t')}
		} as const;
		export interface Action_Outputs {
			${registry.specs
				.map(
					(spec) =>
						`${spec.method}: z.infer<typeof specs.${to_action_spec_output_identifier(spec.method)}>`,
				)
				.join(';\n\t\t\t')}
		}

		/**
		 * Action event data types indexed by method name.
		 * These represent the full discriminated union of all possible states
		 * for each action's event data, properly typed with inputs and outputs.
		 */
		export interface Action_Event_Datas {
			${registry.specs
				.map((spec) => {
					const dataType =
						spec.kind === 'request_response'
							? `Request_Response_Action_Event_Data<'${spec.method}'>`
							: spec.kind === 'remote_notification'
								? `Remote_Notification_Action_Event_Data<'${spec.method}'>`
								: `Local_Call_Action_Event_Data<'${spec.method}'>`;
					return `${spec.method}: ${dataType}`;
				})
				.join(';\n\t\t\t')}
		}

		/**
		 * Parse action params with validation.
		 */
		export const parse_action_input = <T_Method extends keyof typeof Action_Inputs>(
			method: T_Method,
			data: unknown
		): Action_Inputs[T_Method] =>
			Action_Inputs[method].parse(data) as Action_Inputs[T_Method];

		/**
		 * Parse action result with validation.
		 */
		export const parse_action_output = <T_Method extends keyof typeof Action_Outputs>(
			method: T_Method,
			data: unknown
		): Action_Outputs[T_Method] =>
			Action_Outputs[method].parse(data) as Action_Outputs[T_Method];

		/**
		 * Safe parse action params.
		 */
		export const safe_parse_action_input = <T_Method extends keyof typeof Action_Inputs>(
			method: T_Method,
			data: unknown
		): z.SafeParseReturnType<unknown, Action_Inputs[T_Method]> =>
			Action_Inputs[method].safeParse(data) as z.SafeParseReturnType<unknown, Action_Inputs[T_Method]>;

		/**
		 * Safe parse action result.
		 */
		export const safe_parse_action_output = <T_Method extends keyof typeof Action_Outputs>(
			method: T_Method,
			data: unknown
		): z.SafeParseReturnType<unknown, Action_Outputs[T_Method]> =>
			Action_Outputs[method].safeParse(data) as z.SafeParseReturnType<unknown, Action_Outputs[T_Method]>;

		// ${banner}
	`;
};
